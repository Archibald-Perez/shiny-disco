public class Domino {
    public int number;
    public char zone1;
    public char zone2;
    public int crown1;
    public int crown2;

    public Domino(int number, char zone1, char zone2, int crown1, int crown2){
        this.number = number;
        this.zone1= zone1;
        this.zone2 = zone2;
        this.crown1= crown1;
        this.crown2 = crown2;
    }
}
